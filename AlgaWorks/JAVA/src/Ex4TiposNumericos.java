
public class Ex4TiposNumericos {

	public static void main(String[] args) {
		Byte numeroByte = 127;
		// Range do Byte "-128" até "127"

		Short numeroShort = 32767;
		// Range do Short "-32768" até "32767"

		Integer numeroInteger = 2147483647;
		// Range de Integer = " -2³¹ (2147483648) até 2³¹-1 (2147483647)

		Long numeroLong = 1L;
		// Range do Long "-9,223372037×10¹⁸" até "9,223372037×10¹⁸"

		Float numeroFloat = 1.0f;

		Double numeroDouble = 1.0;
	}
}
